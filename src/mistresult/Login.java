    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usernameString=logInUser.getText();
        String passwordString=logInPassword.getText();
        
        if(usernameString.length()==0 || passwordString.length()==0)
        {
            JOptionPane.showMessageDialog(this, "Correct the Input .","WRONG ENTRY !!",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String sql = "Select * from users where  username like '"+usernameString+"' and password like '"+passwordString+"'";
            if(DatabaseHelper.find(sql)==true)
            {
                Mainpanel obj=new  Mainpanel();
                obj.createshow();
                
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Invalid Credentials","WRONG ENTRY",JOptionPane.INFORMATION_MESSAGE);
                logInPassword.setText("");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

