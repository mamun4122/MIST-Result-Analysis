    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        GpaRangeTable obj = new GpaRangeTable();
        obj.tableclear();

        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Give Correct Input");
        } else {
            Map<String, String> studentinfo = ob.getallid(jComboBox2.getSelectedItem().toString());
            Map<String, String> student = new TreeMap<String, String>(studentinfo);
            String selectedsemester = ob.gettermid(jComboBox3.getSelectedItem().toString());

            double temp1 = Double.parseDouble(jTextField1.getText());
            double temp2 = Double.parseDouble(jTextField2.getText());

            double low = Math.min(temp1, temp2);
            double high = Math.max(temp1, temp2);
            if (low < 0 || low > 4 || high > 4 || high < 0) {
                JOptionPane.showMessageDialog(null, "Please Give valid Input");
            } else {
                for (Map.Entry<String, String> mp : student.entrySet()) {
                    double val = ob.getGPA(mp.getValue(), selectedsemester);
                    if (val >= low && val <= high) {
                        obj.addinfo(mp.getKey(), ob.getstudentname(mp.getKey()), val);
                    }
                }
            }
        }
        jPanel2.removeAll();
        jPanel2.repaint();
        jPanel2.add(new JScrollPane(obj));
        jPanel2.repaint();
        jPanel2.revalidate();
        
    }//GEN-LAST:event_jButton1ActionPerformed

